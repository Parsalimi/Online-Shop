class Users:
    def __init__(self, id, username, password, fname, lname, phone, isPhoneVerifyed, nationalId, dateOfBirth, role, lastLoginDate):
        self.id = id
        self.username = username
        self.password = password
        self.fname = fname
        self.lname = lname
        self.phone = phone
        self.isPhoneVerifyed = isPhoneVerifyed
        self.nationalId = nationalId
        self.dateOfBirth = dateOfBirth
        self.role = role
        self.lastLoginDate = lastLoginDate